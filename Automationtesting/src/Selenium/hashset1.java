package Selenium;

import java.util.HashSet;

public class hashset1{
	public static void main(String[] args) {
		HashSet<String>hs=new HashSet<String>();
		hs.add("SQL");
		hs.add("JAVA");
		hs.add("MT");
		hs.add("AT");
		hs.add("PYTHON");
		for(String a:hs)
		{
			System.out.println(a);
		}
	}
	

}
